/// #{{{ @func isCacheDefined
/**
 * @public
 * @param {string} key
 * @return {boolean}
 */
function isCacheDefined(key) {

  if (!arguments.length)
    throw setNoArgError(new Error, 'key');
  if ( !isString(key) )
    throw setTypeError(new TypeError, 'key', 'string');
  if (!key)
    throw setEmptyError(new Error, 'key');

  return hasOwnProperty(global, key) && isObject(global[key]);
}
/// #}}} @func isCacheDefined

